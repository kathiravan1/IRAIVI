def call_counterspeech_agent(llm, text, temperature):
    prompt = f"""நீங்கள் ஒரு அமைதியான மற்றும் மரியாதையான எதிர்வினைப் பேச்சு உருவாக்குபவர்.

பணி: கீழே உள்ள வெறுப்புப் பேச்சுக்கு தமிழில் counterspeech உருவாக்கவும்.

மேலும், counterspeech உருவாக்க காரணமான ஆதார உரை வரிகளை JSON வடிவில் வழங்கவும்.

மேலும், counterspeech தரம் மற்றும் பொருத்தத்துக்கு 0.0 முதல் 1.0 வரை நம்பகத்தன்மை மதிப்பீட்டை வழங்கவும்.

வெறுப்பு உரை: "{text}"

JSON வடிவில் மட்டும் வெளியீடு வேண்டும்:

{{
  "counterspeech": "...",
  "confidence": 0.0 - 1.0,
  "evidence": ["..."]
}}"""

    response = llm.invoke([{"role": "user", "content": prompt}], temperature=temperature)
    try:
        obj = json.loads(response.content)
        return obj.get("counterspeech"), obj.get("confidence", 0.0), obj.get("evidence", [])
    except Exception:
        return None, 0.0, []

def majority_vote_texts(texts):
    c = Counter(texts)
    return c.most_common(1)[0] if texts else None

def aggregate_evidence(lists):
    sp = [e for sublist in lists for e in sublist]
    return list(set(sp))

async def generate_counterspeech_sc(llm, text, k=5):
    texts, confs, evids = [], [], []
    for i in range(k):
        temp = 0.7 + 0.05 * i
        t, c, e = call_counterspeech_agent(llm, text, temp)
        if t:
            texts.append(t)
            confs.append(c)
            evids.append(e)
    return majority_vote_texts(texts), (sum(confs) / len(confs)) if confs else 0.0, aggregate_evidence(evids)

