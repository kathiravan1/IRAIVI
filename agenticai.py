    text = item.get('text', '')
    sections = re.split(r"(?=\d{3,}[A-Z]?(?:\.\d+)?\.)", text)
    for sec in [s.strip() for s in sections if s.strip()]:
        ipc_chunks.append(f"{title} {sec}")

