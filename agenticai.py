def load_json_safe(path: str, default_data: list = None):
    try:
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
            return data if data else default_data or []
    except (FileNotFoundError, json.JSONDecodeError) as e:
        logging.warning(f"Error loading {path}: {e}. Using default data.")
        return default_data or []

