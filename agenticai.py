def retrieve_alpaca_examples(query, top_k=3):
    try:
        if not alpaca_texts or not alpaca_emb or not alpaca_index:
            return []
        q_emb = embed_model.encode([query], convert_to_numpy=True)
        _, I = alpaca_index.search(q_emb, top_k)
        return [(alpaca_texts[i], alpaca_outputs[i]) for i in I[0]]
    except Exception as e:
        logging.error(f"Error retrieving Alpaca examples: {e}")
        return []

