# ------------------ Config ------------------
TAMIL_BLOCK = r"\u0B80-\u0BFF"
TA_CLASS = r"[஀-௿]"  # broad Tamil range

# Seed lists (edit/extend as you wish)
SEXUAL_THREAT = set("""கற்பழிக்கிறேன் கற்பழிப்பேன் கற்பழி வல்லுறவு கிழிக்கிறேன் கிழிப்பேன் துஷ்பிரயோகம் பாலியல் வன்முறை""".split())
