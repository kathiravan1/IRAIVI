# ---------------- Helpers -------------------
def normalize_text(s: str) -> str:
    """Keep Tamil letters, digits, underscores; remove other punctuation; collapse spaces."""
    s = "" if not isinstance(s, str) else s
    s = re.sub(rf"[^\w\s{TAMIL_BLOCK}]", " ", s, flags=re.UNICODE)
    s = re.sub(r"\s+", " ", s).strip()
    return s

def detect_text_column(df) -> str:
    """Find a likely text column, or default to the first column."""
    candidates = [c for c in df.columns if str(c).strip().lower() in {"text", "utterance", "comment", "sentence"}]
    return candidates[0] if candidates else df.columns[0]

def ngrams(tokens, n):
    iters = tee(tokens, n)
    for i, it in enumerate(iters):
        for _ in range(i):
            next(it, None)
    return zip(*iters)

def extract_top_ngrams(series_texts, topk=300, n_values=(1,2,3)):
    counter = Counter()
    for t in series_texts:
        if not isinstance(t, str):
            continue
        toks = t.split()
        for n in n_values:
            for ng in ngrams(toks, n):
                phrase = " ".join(ng)
                if len(phrase) < 2:
                    continue
                if re.fullmatch(r"\d+", phrase):
                    continue
                counter[phrase] += 1
    return counter.most_common(topk)

def is_sexual_or_adult_phrase(phrase: str) -> str | None:
    """
    Return subcategory if the phrase is sexual/adult.
    Subcategories:
      - Sexual Threat
      - Sexual Objectification
      - Sexual/Adult Slur or Content
    """
    toks = set(phrase.split())
    # fast checks
    if toks & SEXUAL_THREAT:
        return "Sexual Threat"
    if toks & SEXUAL_OBJECT:
        return "Sexual Objectification"
    if (toks & SEXUAL_SLURS) or (toks & ADULT_GENERAL):
        return "Sexual/Adult Slur or Content"

    # regex/stem checks
    if re.search(r"(கற்பழி|வல்லுறவு|கிழி)", phrase):
        return "Sexual Threat"
    if re.search(r"(முலை|இடுப்பு|காமம்|உடம்பு|கவர்ச்சி)", phrase):
        return "Sexual Objectification"
    if re.search(r"(ஒத்தா|புண்டை|வேசி|அசிங்கம்|கேவலம்)", phrase):
        return "Sexual/Adult Slur or Content"
    if any(stem in phrase.lower() for stem in EN_SEXUAL_STEMS):
        return "Sexual/Adult Slur or Content"

    return None

def to_placeholders(phrase: str) -> str:
    """
    Replace known tokens by placeholders:
      {SEXUAL_THREAT}, {SEXUAL_ATTR}, {SEX_ADULT_SLUR}, {LATIN_WORD}, {NUMBER}
    """
    out = []
    for w in phrase.split():
        lw = w.lower()
        repl = None
        if lw in SEXUAL_THREAT:
            repl = "{SEXUAL_THREAT}"
        elif lw in SEXUAL_OBJECT:
            repl = "{SEXUAL_ATTR}"
        elif lw in SEXUAL_SLURS or lw in ADULT_GENERAL or any(lw.startswith(s) for s in EN_SEXUAL_STEMS):
            repl = "{SEX_ADULT_SLUR}"
        elif re.fullmatch(r"[A-Za-z]+", w):
            repl = "{LATIN_WORD}"
        elif re.fullmatch(r"\d+", w):
            repl = "{NUMBER}"
        out.append(repl if repl else w)
    return " ".join(out)

