def call_counterspeech_agent(llm, text, temperature):
    prompt = f"""நீங்கள் ஒரு அமைதியான மற்றும் மரியாதையான எதிர்வினைப் பேச்சு உருவாக்குபவர்.

பணி: கீழே உள்ள வெறுப்புப் பேச்சுக்கு தமிழில் counterspeech உருவாக்கவும்.

மேலும், counterspeech உருவாக்க காரணமான ஆதார உரை வரிகளை JSON வடிவில் வழங்கவும்.

மேலும், counterspeech தரம் மற்றும் பொருத்தத்துக்கு 0.0 முதல் 1.0 வரை நம்பகத்தன்மை மதிப்பீட்டை வழங்கவும்.

வெறுப்பு உரை: "{text}"

JSON வடிவில் மட்டும் வெளியீடு வேண்டும்:

{{
  "counterspeech": "...",
  "confidence": 0.0 - 1.0,
  "evidence": ["..."]
}}"""

    response = llm.invoke([{"role": "user", "content": prompt}], temperature=temperature)
    try:
        output = json.loads(response.content)
        return output.get("counterspeech"), output.get("confidence", 0.0), output.get("evidence", [])
    except Exception:
        return None, 0.0, []

def majority_vote_texts(texts):
    counter = Counter(texts)
    most_common = counter.most_common(1)
    return most_common[0] if most_common else None

def aggregate_evidence(evidence_lists):
    all_spans = [span for evlist in evidence_lists for span in evlist]
    return list(set(all_spans))

async def generate_counterspeech_sc(llm, text, k=5):
    counterspeech_texts = []
    confidences = []
    evidences = []

    for i in range(k):
        temperature = 0.7 + 0.05 * i
        text_out, conf, ev = call_counterspeech_agent(llm, text, temperature)
        if text_out:
            counterspeech_texts.append(text_out)
            confidences.append(conf)
            evidences.append(ev or [])

    final_text = majority_vote_texts(counterspeech_texts)
    avg_conf = sum(confidences) / len(confidences) if confidences else 0.0
    final_evidence = aggregate_evidence(evidences)

    return final_text, avg_conf, final_evidence

