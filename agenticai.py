def retrieve_alpaca_examples(query, top_k=3):
    q_emb = get_query_embedding(query)
    _, I = alpaca_index.search(q_emb, top_k)
    return [(alpaca_texts[i], alpaca_outputs[i]) for i in I[0]]

# ===== IPC Embedding Index =====
ipc_data = load_json_safe(IPC_JSON_PATH, DEFAULT_IPC_DATA)
ipc_chunks = []
for item in ipc_data:
    title = item.get('title', '')
    text = item.get('text', '')
    sections = re.split(r"(?=\d{3,}[A-Z]?(?:\.\d+)?\.)", text)
    for sec in [s.strip() for s in sections if s.strip()]:
        ipc_chunks.append(f"{title} {sec}")
ipc_emb = embed_model.encode(ipc_chunks, convert_to_numpy=True)
ipc_index = faiss.IndexFlatL2(ipc_emb.shape[1])
ipc_index.add(ipc_emb)

def retrieve_ipc_sections(query, top_k=3):
    q_emb = get_query_embedding(query)
    _, I = ipc_index.search(q_emb, top_k)
    return [ipc_chunks[i] for i in I]

