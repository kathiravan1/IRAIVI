async def main():
    sample_text = "இரண்டு பேரின் (புண்டையை) சாமான்களை கோணிய ஊசால் தைக்க வேண்டும். இரண்டு பேரும் புறாடுகள் போல் தெரிகிறது."
    logging.info(f"Processing sample: {sample_text}")

    results = {}

    # Step 1: Hate or Non-Hate?
    hate_prompt = f"Classify as hate or non-hate: {sample_text}\nSimilar: {retrieve_alpaca_examples(sample_text)}\nClassification:"
    results['hate_or_not'] = hate_classifier.llm(hate_prompt)
    print(f"Hate classification: {results['hate_or_not']}")

    # Step 2: If hate, classify further
    if results['hate_or_not'].strip().lower() == "hate":
        # Step 3: Explicit Hate Classification
        explicit_prompt = f"Classify this Tamil hate speech into: sexual_threat, sexual_objectification, or verbal_abuse.\nText: {sample_text}\nCategory:"
        results['explicit'] = explicit_hate.llm(explicit_prompt)
        print(f"Explicit category: {results['explicit']}")

        # Conditional Step 4: Implicit Hate Classification (SKIP if explicit is detected)
        explicit_categories = ['sexual_threat', 'sexual_objectification', 'verbal_abuse']
        if results['explicit'].strip().lower() in explicit_categories:
            logging.info("Explicit hate detected, skipping implicit hate classification.")
            results['implicit'] = None
        else:
            implicit_prompt = f"Classify this Tamil hate speech into: victim_blaming, character_assassination, or moral_policing.\nText: {sample_text}\nCategory:"
            results['implicit'] = implicit_hate.llm(implicit_prompt)
            print(f"Implicit category: {results['implicit']}")

        # Step 5: Bias Detection
        bias_prompt = f"Detect if this Tamil text contains any of the following biases (comma-separated list or 'no_bias'): caste_bias, age_bias, cultural_bias, geographical_bias.\nText: {sample_text}\nBiases:"
        results['biases'] = bias_detection.llm(bias_prompt)
        print(f"Biases: {results['biases']}")

        # Step 6: IPC Retrieval (based on explicit category)
        ipc_refs = retrieve_ipc_sections(results['explicit'])
        results['ipc_clauses'] = ipc_refs
        print("IPC refs:", results['ipc_clauses'])

        # Step 7: Counterspeech Generation
        counter_prompt = f"""Generate calm, respectful counterspeech in Tamil for: {sample_text}
Explicit: {results['explicit']}
Implicit: {results['implicit'] if results['implicit'] else 'None'}
Biases: {results['biases']}
IPC: {results['ipc_clauses']}
Respond in calm, respectful Tamil.
Counterspeech:"""
        results['counterspeech'] = counter_speech.llm(counter_prompt)
        print("Counterspeech:", results['counterspeech'])

    else:
        results.update({k: None for k in ['explicit', 'implicit', 'biases', 'ipc_clauses', 'counterspeech']})
        print("Text is not hate speech. Skipping further analysis.")

    print("\nFull analysis result:")
    # Use json.dumps with ensure_ascii=False to print Tamil characters correctly
    print(json.dumps(results, indent=2, ensure_ascii=False))


